    public DepartmentalComplaints() {
    }

    public String getDepartmentalcomplaints() {
        return departmentalcomplaints;
    }

    public void setDepartmentalcomplaints(String departmentalcomplaints) {
        this.departmentalcomplaints = departmentalcomplaints;
    }

