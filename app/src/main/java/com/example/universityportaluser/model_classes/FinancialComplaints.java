package com.example.universityportaluser.model_classes;

public class FinancialComplaints {
    String financialcomplaint,userid,key,time;

    public FinancialComplaints() {
    }

    public String getFinancialcomplaint() {
        return financialcomplaint;
    }

    public void setFinancialcomplaint(String financialcomplaint) {
        this.financialcomplaint = financialcomplaint;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
